#include <stdio.h>
void PrintInteger(int n)
{
	for (int i = 0; i < n; ++i)
		printf("Hello : %d ", i);
	printf("\n");
}
int main()
{
	PrintInteger(100);
	PrintInteger(100);
	PrintInteger(100);
}

